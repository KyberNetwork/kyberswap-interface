const filterAgents = (data: AgentCard[], query?: LeaderboardSummaryQuery | LeaderboardQuery | AgentsQuery) =>
  data.filter(agent => {
    if (query?.chainId && !agent.chains.some(chain => chain.chainId === query.chainId)) return false
    if (query?.strategy && agent.strategy !== query.strategy) return false
    if (query?.risk && agent.risk !== query.risk) return false
    if (query?.search && !agent.displayName.toLowerCase().includes(query.search.toLowerCase())) return false
    return true
  })

const filterPositions = (data: PositionSummary[], status?: 'all' | PositionStatus) =>
  !status || status === 'all' ? data : data.filter(position => position.status === status)

